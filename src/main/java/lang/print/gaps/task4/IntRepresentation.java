package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        char c = 99;
        char h = 104;
        char a = 97;
        char r = 114;

        int charC = (char) c;
        int charH = (char) h;
        int charA = (char) a;
        int charR = (char) r;
        
        System.out.println(charC);
        System.out.println(charH);
        System.out.println(charA);
        System.out.println(charR);
    }
}
